name = "Logic first"
print(name[4])      # c
print(name[0:4])    # Logi
print(name[2:4])    # gi
print(name[2:])     # gic first
print(name[2:10:2]) # gcfr
print(name[2:10:3]) # g r

print(name[-2])     # s
print(name[-5:-2])  # fir

#str reverse
print(name[::-1])

#using slice method
x = slice(0,5)
print(name[x])

